package vo;

public class Grade {
  private String sid;
  private String stept;
  private String courage;
  private int   grade;

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getStept() {
        return stept;
    }

    public void setStept(String stept) {
        this.stept = stept;
    }

    public String getCourage() {
        return courage;
    }

    public void setCourage(String courage) {
        this.courage = courage;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }
}
